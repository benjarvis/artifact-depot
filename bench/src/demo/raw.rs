use rand::Rng;
use rand::RngCore;

/// Generate raw artifact content appropriate for the given content type.
pub fn generate_content(rng: &mut dyn RngCore, content_type: &str, path: &str) -> Vec<u8> {
    match content_type {
        "text/yaml" => generate_yaml(rng, path),
        "application/json" => generate_json(rng, path),
        "text/plain" => generate_conf(rng),
        "text/markdown" => generate_markdown(rng, path),
        "application/java-archive" | "application/gzip" => generate_archive(rng),
        _ => generate_archive(rng),
    }
}

pub(crate) fn generate_yaml(rng: &mut dyn RngCore, path: &str) -> Vec<u8> {
    let port = rng.gen_range(3000u16..9999);
    let pool_size = rng.gen_range(5u32..50);
    let timeout = rng.gen_range(10u32..120);
    format!(
        r#"# Configuration: {path}
# Auto-generated by depot-bench

server:
  host: 0.0.0.0
  port: {port}
  workers: 4
  keep_alive: 75

database:
  url: postgres://localhost:5432/appdb
  pool_size: {pool_size}
  timeout: {timeout}
  ssl_mode: require

logging:
  level: info
  format: json
  output: stdout

cache:
  enabled: true
  ttl: 3600
  max_entries: 10000

features:
  enable_metrics: true
  enable_tracing: true
  rate_limit: 1000
"#,
    )
    .into_bytes()
}

pub(crate) fn generate_json(rng: &mut dyn RngCore, path: &str) -> Vec<u8> {
    let version = format!(
        "{}.{}.{}",
        rng.gen_range(0u32..5),
        rng.gen_range(0u32..20),
        rng.gen_range(0u32..100)
    );
    if path.contains("terraform") {
        serde_json::json!({
            "terraform": {
                "required_version": ">= 1.0",
                "backend": {
                    "s3": {
                        "bucket": "terraform-state",
                        "key": "infra/state.tfstate",
                        "region": "us-east-1"
                    }
                }
            },
            "provider": {
                "aws": {
                    "region": "us-east-1",
                    "profile": "production"
                }
            }
        })
        .to_string()
        .into_bytes()
    } else {
        serde_json::json!({
            "name": path.split('/').next_back().unwrap_or("package"),
            "version": version,
            "description": "Auto-generated package",
            "main": "index.js",
            "scripts": {
                "start": "node index.js",
                "test": "jest --coverage",
                "build": "tsc && webpack"
            },
            "dependencies": {
                "express": "^4.18.0",
                "lodash": "^4.17.21"
            }
        })
        .to_string()
        .into_bytes()
    }
}

pub(crate) fn generate_conf(rng: &mut dyn RngCore) -> Vec<u8> {
    let port = rng.gen_range(80u16..9999);
    let workers = rng.gen_range(1u32..16);
    format!(
        r#"# Auto-generated configuration
# depot-bench

worker_processes {workers};

events {{
    worker_connections 1024;
}}

http {{
    server {{
        listen {port};
        server_name localhost;

        location / {{
            proxy_pass http://backend:8080;
            proxy_set_header Host $host;
            proxy_set_header X-Real-IP $remote_addr;
        }}

        location /health {{
            return 200 'ok';
        }}
    }}
}}
"#,
    )
    .into_bytes()
}

pub(crate) fn generate_markdown(rng: &mut dyn RngCore, path: &str) -> Vec<u8> {
    let title = path
        .split('/')
        .next_back()
        .unwrap_or("Document")
        .trim_end_matches(".md");
    let sections = rng.gen_range(3u32..8);
    let mut doc = format!("# {}\n\n", title);
    doc.push_str("## Overview\n\n");
    doc.push_str("This document provides essential information about the system architecture and operational procedures.\n\n");

    for i in 1..=sections {
        doc.push_str(&format!("## Section {}\n\n", i));
        doc.push_str("- Key point about system design and reliability\n");
        doc.push_str("- Performance considerations and trade-offs\n");
        doc.push_str("- Operational guidelines for the team\n\n");
        doc.push_str("```\n");
        doc.push_str(&format!("example_command --flag{} value\n", i));
        doc.push_str("```\n\n");
    }
    doc.into_bytes()
}

pub(crate) fn generate_archive(rng: &mut dyn RngCore) -> Vec<u8> {
    let size = rng.gen_range(1024usize..5 * 1024);
    let mut data = vec![0u8; size];
    rng.fill_bytes(&mut data);
    // Put a valid gzip header so it looks realistic
    data[0] = 0x1f;
    data[1] = 0x8b;
    data[2] = 0x08;
    data
}

#[cfg(test)]
mod tests {
    use super::*;
    use rand::SeedableRng;
    use rand_chacha::ChaCha8Rng;

    fn test_rng() -> ChaCha8Rng {
        ChaCha8Rng::seed_from_u64(42)
    }

    #[test]
    fn generate_content_dispatches_yaml() {
        let mut rng = test_rng();
        let content = generate_content(&mut rng, "text/yaml", "test.yml");
        let text = String::from_utf8(content).unwrap();
        assert!(text.contains("server:"));
        assert!(text.contains("database:"));
        assert!(text.contains("port:"));
    }

    #[test]
    fn generate_content_dispatches_json() {
        let mut rng = test_rng();
        let content = generate_content(&mut rng, "application/json", "package.json");
        let val: serde_json::Value = serde_json::from_slice(&content).unwrap();
        assert!(val.get("name").is_some());
        assert!(val.get("version").is_some());
    }

    #[test]
    fn generate_content_dispatches_plaintext() {
        let mut rng = test_rng();
        let content = generate_content(&mut rng, "text/plain", "nginx.conf");
        let text = String::from_utf8(content).unwrap();
        assert!(text.contains("worker_processes"));
        assert!(text.contains("server"));
    }

    #[test]
    fn generate_content_dispatches_markdown() {
        let mut rng = test_rng();
        let content = generate_content(&mut rng, "text/markdown", "docs/README.md");
        let text = String::from_utf8(content).unwrap();
        assert!(text.starts_with("# "));
        assert!(text.contains("## Overview"));
        assert!(text.contains("## Section"));
    }

    #[test]
    fn generate_content_dispatches_archive() {
        let mut rng = test_rng();
        let content = generate_content(&mut rng, "application/java-archive", "app.jar");
        assert!(content.len() >= 1024);
        assert_eq!(content[0], 0x1f);
        assert_eq!(content[1], 0x8b);
    }

    #[test]
    fn generate_content_unknown_type_falls_back_to_archive() {
        let mut rng = test_rng();
        let content = generate_content(&mut rng, "application/octet-stream", "file.bin");
        assert_eq!(content[0], 0x1f);
        assert_eq!(content[1], 0x8b);
    }

    #[test]
    fn generate_yaml_contains_config_path() {
        let mut rng = test_rng();
        let content = generate_yaml(&mut rng, "staging/app.yml");
        let text = String::from_utf8(content).unwrap();
        assert!(text.contains("staging/app.yml"));
    }

    #[test]
    fn generate_yaml_has_valid_structure() {
        let mut rng = test_rng();
        let content = generate_yaml(&mut rng, "test.yml");
        let text = String::from_utf8(content).unwrap();
        assert!(text.contains("logging:"));
        assert!(text.contains("cache:"));
        assert!(text.contains("features:"));
    }

    #[test]
    fn generate_json_terraform_path() {
        let mut rng = test_rng();
        let content = generate_json(&mut rng, "infra/terraform.json");
        let val: serde_json::Value = serde_json::from_slice(&content).unwrap();
        assert!(val.get("terraform").is_some());
        assert!(val.get("provider").is_some());
    }

    #[test]
    fn generate_json_package_path() {
        let mut rng = test_rng();
        let content = generate_json(&mut rng, "libs/mylib/package.json");
        let val: serde_json::Value = serde_json::from_slice(&content).unwrap();
        assert_eq!(val["name"], "package.json");
        assert!(val.get("scripts").is_some());
        assert!(val.get("dependencies").is_some());
    }

    #[test]
    fn generate_conf_has_nginx_structure() {
        let mut rng = test_rng();
        let content = generate_conf(&mut rng);
        let text = String::from_utf8(content).unwrap();
        assert!(text.contains("worker_processes"));
        assert!(text.contains("events {"));
        assert!(text.contains("http {"));
        assert!(text.contains("proxy_pass"));
    }

    #[test]
    fn generate_markdown_uses_filename_as_title() {
        let mut rng = test_rng();
        let content = generate_markdown(&mut rng, "project/v1/README.md");
        let text = String::from_utf8(content).unwrap();
        assert!(text.starts_with("# README\n"));
    }

    #[test]
    fn generate_markdown_has_multiple_sections() {
        let mut rng = test_rng();
        let content = generate_markdown(&mut rng, "doc.md");
        let text = String::from_utf8(content).unwrap();
        let section_count = text.matches("## Section").count();
        assert!(
            section_count >= 3,
            "expected >= 3 sections, got {}",
            section_count
        );
        assert!(
            section_count <= 7,
            "expected <= 7 sections, got {}",
            section_count
        );
    }

    #[test]
    fn generate_markdown_has_code_blocks() {
        let mut rng = test_rng();
        let content = generate_markdown(&mut rng, "doc.md");
        let text = String::from_utf8(content).unwrap();
        assert!(text.contains("```\n"));
        assert!(text.contains("example_command"));
    }

    #[test]
    fn generate_archive_has_gzip_header() {
        let mut rng = test_rng();
        let data = generate_archive(&mut rng);
        assert!(data.len() >= 1024);
        assert!(data.len() < 5 * 1024);
        assert_eq!(data[0], 0x1f);
        assert_eq!(data[1], 0x8b);
        assert_eq!(data[2], 0x08);
    }

    #[test]
    fn generate_archive_deterministic() {
        let d1 = generate_archive(&mut test_rng());
        let d2 = generate_archive(&mut test_rng());
        assert_eq!(d1, d2);
    }

    #[test]
    fn generate_content_deterministic() {
        let c1 = generate_content(&mut test_rng(), "text/yaml", "a.yml");
        let c2 = generate_content(&mut test_rng(), "text/yaml", "a.yml");
        assert_eq!(c1, c2);
    }
}
